public class Stringconcatenation{
    public static void main(String[] args) {
        System.out.println('a' + 'b');
        System.out.println("a" + "b");
        System.out.println((char)('a' + 3)); // Type casting
        System.out.println("a" + 1);
        System.out.println("a" + 'a');
    }
}
